# Menginput
x = float(input("Tuliskan Nilai X : "))
y = float(input("Tuliskan Nilai Y: "))
k = float(input("Tuliskan Nilai Skala (K) : "))

# Mengkonversi
x1 = x * k
y1 = y * k 

# Menampilkan Hasil
print('==========================================================')
print('Maka Hasil Pencerminan A(%d,%d)' %(x,y))
print('Maka X* = %d'  %(x1))
print('Maka Y* = %d'  %(y1))
print('==========================================================')
